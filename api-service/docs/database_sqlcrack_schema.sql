-- ============================================================
-- 03. CORE AUTH TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS roles (
  role_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name role_name NOT NULL UNIQUE,
  description text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS users (
  user_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  email varchar NOT NULL,
  password_hash varchar NOT NULL,
  role_id integer NOT NULL,
  status user_status NOT NULL DEFAULT 'active',
  token_version integer NOT NULL DEFAULT 0,
  last_login_at timestamptz,
  refresh_token_hash text,
  refresh_token_expires_at timestamptz,
  refresh_token_created_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);


-- ============================================================
-- 04. HR MASTER DATA TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS departments (
  department_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar NOT NULL,
  code varchar,
  description text,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS positions (
  position_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar NOT NULL,
  code varchar,
  description text,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);


-- ============================================================
-- 05. EMPLOYEE TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS employees (
  employee_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id uuid,
  registered_by uuid,
  employee_code varchar NOT NULL,
  full_name varchar NOT NULL,
  phone varchar,
  avatar_url text,
  department_id integer,
  position_id integer,
  manager_id uuid,
  date_of_birth date,
  gender varchar,
  address text,
  hire_date date,
  resignation_date date,
  status employee_status NOT NULL DEFAULT 'active',
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
COMMENT ON COLUMN employees.registered_by IS 'User who created this employee profile';
COMMENT ON COLUMN employees.gender IS 'male | female | other';

CREATE TABLE IF NOT EXISTS department_managers (
  manager_id uuid,
  department_id integer,
  assigned_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (manager_id, department_id)
);


-- ============================================================
-- 06. SHIFT / SCHEDULE TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS work_shifts (
  shift_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar NOT NULL,
  code varchar,
  start_time time NOT NULL,
  end_time time NOT NULL,
  is_overnight boolean NOT NULL DEFAULT false,
  late_threshold_minutes integer NOT NULL DEFAULT 0,
  early_leave_threshold_minutes integer NOT NULL DEFAULT 0,
  required_work_minutes integer,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
COMMENT ON COLUMN work_shifts.required_work_minutes IS 'Required working minutes to count as full attendance';

CREATE TABLE IF NOT EXISTS employee_shift_assignments (
  assignment_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_id uuid NOT NULL,
  shift_id integer NOT NULL,
  effective_date date NOT NULL,
  end_date date,
  created_by uuid,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS holidays (
  holiday_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar NOT NULL,
  holiday_date date NOT NULL,
  description varchar,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);


-- ============================================================
-- 07. FACE RECOGNITION TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS face_profiles (
  profile_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  employee_id uuid NOT NULL,
  status face_profile_status NOT NULL DEFAULT 'pending',
  qdrant_collection varchar NOT NULL,
  embedding_model varchar,
  embedding_version varchar,
  registered_by uuid,
  revocation_reason text,
  revoked_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
COMMENT ON COLUMN face_profiles.qdrant_collection IS 'Ten collection trong Qdrant';
COMMENT ON COLUMN face_profiles.embedding_model IS 'e.g. buffalo_l / arcface';
COMMENT ON COLUMN face_profiles.embedding_version IS 'Used when switching embedding model';


-- ============================================================
-- 08. ATTENDANCE TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS attendance_events (
  event_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  employee_id uuid,
  event_type attendance_event_type NOT NULL,
  event_time timestamptz NOT NULL,
  confidence_score double precision,
  anti_spoof_score double precision,
  image_url text,
  raw_result jsonb,
  is_accepted boolean NOT NULL DEFAULT true,
  rejection_reason text,
  created_at timestamptz NOT NULL DEFAULT now()
);
COMMENT ON COLUMN attendance_events.raw_result IS 'Full JSON response from AI service';

CREATE TABLE IF NOT EXISTS attendance_records (
  record_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  employee_id uuid NOT NULL,
  shift_id integer,
  work_date date NOT NULL,
  check_in_time timestamptz,
  check_out_time timestamptz,
  status attendance_record_status NOT NULL,
  late_minutes integer NOT NULL DEFAULT 0,
  early_leave_minutes integer NOT NULL DEFAULT 0,
  worked_minutes integer NOT NULL DEFAULT 0,
  source attendance_source NOT NULL DEFAULT 'face_recognition',
  notes text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);
COMMENT ON COLUMN attendance_records.work_date IS 'Business date. Model comment says this should be date; current SQLAlchemy declaration omits an explicit Date type.';


-- ============================================================
-- 09. LEAVE MANAGEMENT TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS leave_types (
  leave_type_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar NOT NULL,
  code varchar,
  is_paid boolean NOT NULL DEFAULT true,
  max_days_per_year integer,
  description text,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS leave_requests (
  request_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  employee_id uuid NOT NULL,
  leave_type_id integer NOT NULL,
  start_date date NOT NULL,
  end_date date NOT NULL,
  time_type leave_time_type NOT NULL DEFAULT 'full_day',
  total_days double precision,
  reason text,
  status leave_request_status NOT NULL DEFAULT 'pending',
  approved_by uuid,
  approved_at timestamptz,
  rejection_reason text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS leave_approval_logs (
  log_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  leave_request_id uuid NOT NULL,
  approver_id uuid NOT NULL,
  action approval_action NOT NULL,
  comment text,
  created_at timestamptz NOT NULL DEFAULT now()
);


-- ============================================================
-- 10. ATTENDANCE CORRECTION TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS attendance_correction_requests (
  request_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  employee_id uuid NOT NULL,
  attendance_record_id uuid,
  requested_check_in timestamptz,
  requested_check_out timestamptz,
  reason text NOT NULL,
  status correction_request_status NOT NULL DEFAULT 'pending',
  reviewed_by uuid,
  reviewed_at timestamptz,
  rejection_reason text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS attendance_correction_logs (
  log_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  correction_request_id uuid NOT NULL,
  reviewer_id uuid NOT NULL,
  action approval_action NOT NULL,
  comment text,
  old_check_in timestamptz,
  old_check_out timestamptz,
  new_check_in timestamptz,
  new_check_out timestamptz,
  created_at timestamptz NOT NULL DEFAULT now()
);


-- ============================================================
-- 11. NOTIFICATION / AUDIT / SETTINGS TABLES
-- ============================================================
CREATE TABLE IF NOT EXISTS notifications (
  notification_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id uuid NOT NULL,
  title varchar NOT NULL,
  content text,
  type notification_type NOT NULL,
  object_type varchar,
  object_id varchar,
  is_read boolean NOT NULL DEFAULT false,
  read_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now()
);
COMMENT ON COLUMN notifications.object_type IS 'leave_request | correction_request | attendance_record | face_profile';

CREATE TABLE IF NOT EXISTS audit_logs (
  log_id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  performed_by uuid,
  action audit_action NOT NULL,
  object_type varchar NOT NULL,
  object_id varchar,
  old_value jsonb,
  new_value jsonb,
  reason text,
  ip_address varchar,
  user_agent text,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS system_settings (
  setting_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  key varchar NOT NULL,
  value jsonb NOT NULL,
  description text,
  updated_by uuid,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);


