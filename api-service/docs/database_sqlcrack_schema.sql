-- ============================================================
-- SQL Crack Friendly Format
-- Source: database.dbml
-- Target: PostgreSQL
-- Purpose:
--   1. Keep every statement separated by semicolon.
--   2. Group schema by domain: auth, HR, shifts, face, attendance, leave.
--   3. Make FOREIGN KEY and INDEX statements multi-line for easier reading.
-- Note:
--   SQL Crack is strongest for SELECT/JOIN/CTE query flow visualization.
--   For ERD/schema visualization, DBeaver, DataGrip, pgAdmin, or dbdiagram.io
--   are usually better.
-- ============================================================

